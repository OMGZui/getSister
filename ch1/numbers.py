
# + - * / 重点讲下 /

print(8/5)  # 1.6  division always returns a floating point number
print(17/3) # 5.666666666666667 经典除返回一个浮点数 classic division returns a float
print(17//3) # 5 地板除丢弃小数部分 floor division discards the fractional part
print(17%3) # 2 求余

print(2 ** 10) # 2的10次方 2 to the power of 101+1